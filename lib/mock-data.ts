export function buildMockCreatedSignal(input: SignalCreatePayload): SignalRecord {
  return createSignalRecord({
    recordId: `mock_sig_${Date.now()}`,
    createdDate: new Date().toISOString(),
    createdBy: input.createdBy ?? "Mock Operator",
    sourceTitle: input.sourceTitle,
    sourceUrl: input.sourceUrl ?? null,
    sourceType: input.sourceType ?? "Other",
    sourcePublisher: input.sourcePublisher ?? "Manual Intake",
    sourceDate: input.sourceDate ?? null,
    rawExcerpt: input.rawExcerpt ?? null,
    manualSummary: input.manualSummary ?? null,
    scenarioAngle: input.scenarioAngle ?? null,
    signalCategory: input.signalCategory ?? null,
    severityScore: input.severityScore ?? null,
    hookTemplateUsed: input.hookTemplateUsed ?? null,
    ingestionSource: input.ingestionSource ?? null,
    ingestionMethod: input.ingestionMethod ?? null,
    keepRejectRecommendation: input.keepRejectRecommendation ?? null,
    whySelected: input.whySelected ?? null,
    autoGenerated: input.autoGenerated ?? null,
    needsHumanReview: input.needsHumanReview ?? null,
    reviewPriority: input.reviewPriority ?? null,
    status: input.status,
  });
}

export function addMockSignalRecord(input: SignalCreatePayload): SignalRecord {
  const signal = buildMockCreatedSignal(input);
  mockSignalRecords.unshift(signal);
  return signal;
}

export function getMockSignalById(recordId: string): SignalRecord | null {
  return mockSignalRecords.find((signal) => signal.recordId === recordId) ?? null;
}

export function buildMockUpdatedSignal(recordId: string, updates: Partial<SignalRecord>): SignalRecord | null {
  const existing = getMockSignalById(recordId);
  if (!existing) {
    return null;
  }

  return {
    ...existing,
    ...updates,
    recordId: existing.recordId,
  };
}
